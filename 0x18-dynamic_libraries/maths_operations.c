#include <stdio.h>

/**
 * @add(): function that add 2 int together
 * @sub(): function that subtract 1 int from other
 * @mul(): function that multiples 2 int
 * @div(): function that divides 2 int
 * @mod(): function that finds the modulus of 2 int
 */

int add(int a, int b)
{
	return (a + b);
}

int sub(int a, int b)
{
	return (a - b);
}

int mul(int a, int b)
{
	return (a * b);
}

int div(int a, int b)
{
	return (a / b);
}

int mod(int a, int b)
{
	return (a % b);
}
